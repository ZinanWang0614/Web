package com.me.pojo;

/**
 *
 * @author zinanwang
 */
public class Item {
    
    private String name;
    private float price;
    private String type;

    public Item() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
    
    
       
}
